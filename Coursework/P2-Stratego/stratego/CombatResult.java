package stratego;

/**
 *  Combat results that is possible after an attack is performed.
 */
public enum CombatResult {
     /**
     * Indicates an attack has resulted in a successfull win .
     */
    WIN,
     /**
     * Indicates an attack has resulted in loss.
     */
    LOSE,
     /**
     * Indicates an attack has resulted in draw.
     */
    DRAW
}
